package com.example.hackathonchellenge;

public class TradeModel {

    public String year;
    public String reservesChina;
    public String reservesIndia;
    public String reservesUSA;
    public String debtChina;
    public String debtIndia;
    public String debtUSA;
    public String GNIChina;
    public String GNIIndia;
    public String GNIUSA;
    public String CurrentGNIChina;
    public String CurrentGNIIndia;
    public String CurrentGNIUSA;




    public TradeModel(String year, String reservesChina, String reservesIndia,
                      String reservesUSA, String debtChina, String debtIndia,
                      String debtUSA, String GNIChina, String GNIIndia,
                      String GNIUSA, String CurrentGNIChina, String CurrentGNIIndia,
                      String CurrentGNIUSA) {
        this.year = year;
        this.reservesChina = reservesChina;
        this.reservesIndia = reservesIndia;
        this.reservesUSA = reservesUSA;
        this.debtChina = debtChina;
        this.debtIndia = debtIndia;
        this.debtUSA = debtUSA;
        this.GNIUSA = GNIUSA;
        this.GNIChina = GNIChina;
        this.GNIIndia = GNIIndia;
        this.CurrentGNIChina = CurrentGNIChina;
        this.CurrentGNIIndia = CurrentGNIIndia;
        this.CurrentGNIUSA = CurrentGNIUSA;
    }



}

